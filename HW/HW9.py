s = input('String? ')
print('третий символ строки:',s[2])
print('выведите предпоследний символ строки:', s[-2])
print('первые пять символов строки:', s[0:5])
print('строка, кроме последних двух символов:', s[0:-2])
print('символы с четными индексами:', s[::2])
print('символы с нечетными индексами:', s[1::2])
print('символы в обратном порядке:', s[::-1])
print('все символы строки через один в обратном порядке:', s[::-2])
print('длина строки:', len(s))
